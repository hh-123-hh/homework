package section02;

public class EX21 {

	public static void main(String[] args) {
		// 2-21
	       int num11 = 11; // num에 정수 11을 대입한다
	       double num21 = 3.14; // num에 실수 3.14를 대입한다.
	       double num1Change = (double)num11; // num11을 double로 값 을 변환하여 num1Change에 저장

	       System.out.println("num1 : " + num11); // num1 출력
	       System.out.println("num1Changem : " + num1Change); // num1Change 출력
	       System.out.println("num1Changem : " + num21); // num1Changem : " + num21 출력
	       System.out.println((int)1.23); // 형변환값 출력
	       
	       // ▼ 결과 값 ▼
//	       num1 : 11
//	       num1Changem : 11.0
//	       num1Changem : 3.14

	}

}
