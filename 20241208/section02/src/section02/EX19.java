package section02;

public class EX19 {

	public static void main(String[] args) {
		// 2-19
      boolean isStudent = true; // 조건식의 결과가 참이면 if문이 실행되고 else문은 실행 되지 않는다.

      if(isStudent) { // isStudent 가 true이면, 
         System.out.println("저는 학생입니다."); // true
      } else { // isStudent 가 false 이면,
         System.out.println("저는 학생이 아닙니다."); // false
      }
      	// 결과 값 : 저는 학생입니다.
	}

}
