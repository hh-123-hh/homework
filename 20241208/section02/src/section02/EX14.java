package section02;

public class EX14 {

	public static void main(String[] args) {
      double pieDouble = 3.141592653589793; // 출력 값 : 3.141592653589793, / 자료형 실수의 기본값으로 웬만하면 double로 사용하기.
      
      float piefloat = 3.141592653589793f; // 소수점 7자리 까지 출력, 나머지 자리는 버려짐
         
      System.out.println("double : " + pieDouble);
      System.out.println("float : " + piefloat);

     
//      ▼▼ 결과 값 
//      double : 3.141592653589793
//      float : 3.1415927

	}

}
