package section02;

public class EX12 {
	public static void main(String[] args) {
	      // 2-12
      byte numb1 = 0110; // 2진수
      short numb2 = 0107; // 8진수
      int numb3 = 0x46; // 16진수
      long numb4=69L; // 10진수 정수
      
      System.out.println(numb1);
      System.out.println(numb2);
      System.out.println(numb3);
      System.out.println(numb4); 		// ===> 2진수,8진수, 16진수, 10진수 출력법도 있지만, 기본 출력 형태는 10진수 형태인 정수 형태이다.
	}
}
