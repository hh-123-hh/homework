package section02;

public class ConsolePrint {

	public static void main(String[] args) {
		// 2-1
		System.out.print("Welcome"); // "Welcome"을 줄바꿈 하여출력
		System.out.print("java World"); // "java world"를 줄바꿈 없이 출력
//       "Welcome." 문자열 옆에 "Java World" 를 출력

		// 2-2
		System.out.println("Welcome"); // "Welcome"을 줄바꿈 하여 출력
		// 괄호 ()안의 데이터를 콘솔창에 출력
		System.out.println("Java World"); // "Java World"를 줄바꿈 하여 출력

	}
}
