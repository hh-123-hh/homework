package section03;

public class EX13 {
	public static void main(String[] args) {
      // 3-13
      int a = 10;
      int b = 20;

      System.out.println(a > b);
      System.out.println(a <= b);
      System.out.println(a == b);
      System.out.println(a != b);
      
      // ▼▼ 결과값 ▼▼
      //(a는 b보다 작으므로)   false
      //(a는 b보다 크므로)    true
      //(a는 b보다 작으므로)   false
      //(a는 b와 같지않으므로)  true
      
	}
}
