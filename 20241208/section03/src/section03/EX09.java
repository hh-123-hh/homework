package section03;

public class EX09 {
	public static void main(String[] args) {
      // 3-9
      int x = 100;
      int y = 200;

      System.out.println(x + y); // 100 + 200
      System.out.println(x - y); // 100 - 200
      System.out.println(x * y); // 100 * 200
      System.out.println(x / y); // 100 / 200 정수형의 계산으로 0.5가 아닌 0을 반환
      System.out.println(x % y); // 100 % 200
      
      // ▼▼ 결과값 ▼▼
//      300			
//      -100
//      20000
//      0
//      100			


	}
}
