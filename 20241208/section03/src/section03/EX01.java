package section03;

public class EX01 {

	public static void main(String[] args) {
		int x = 100; // x 의 값은 정수 100이다. 선언
		
		int resultPlus = +x; // +부호
		// 인트 변수 값은 + 부호 가 붙은 결과 값이다.  선언
		int resultMinus = -x; // -부호
		// 인트 변수 값은 - 부호 가 붙은 결과 값이다.  선언
		System.out.println(resultPlus);
		System.out.println(resultMinus);

//  ▼ 결과 값 ▼     
//      100
//     -100

	}

}
