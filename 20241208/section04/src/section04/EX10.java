package section04;

public class EX10 {
	public static void main(String[] args) {
		 int num = 7;
	      
	      switch(num) {
	      case 1:
	         System.out.println("num은 1입니다.");  
	         break;
	      case 7:
	         System.out.println("num은 7입니다.");  
	         break;
	      default :
	         System.out.println("num은 1도 7아닙니다."); 
	         
	      // ▼▼ 결과값 ▼▼
	      // num은 7입니다. -> num의 값은 7이므로 해당 결과 값이 나옴
	      }
	   }
	}